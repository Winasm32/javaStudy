package integerVariable;

public class 문자리터럴 {
	
	public static void main(String[] args) {
        char ch = 'a'; // 한 따옴표 사용! 
        char c = '\u0061'; // 유니코드 표현법
        System.out.println(ch);  
        System.out.println(c); 
        // 특수문자 백슬래시(\)를 사용하여 " 표시함
        System.out.println("\"  is a symbol");
	}
}
